-- 확인: 모든 퀴즈에 문제가 추가되었는지 확인
SELECT
  q.id as quiz_id,
  q.title as quiz_title,
  COUNT(qq.id) as question_count
FROM public.quizzes q
LEFT JOIN public.quiz_questions qq ON q.id = qq.quiz_id
GROUP BY q.id, q.title
ORDER BY q.id;
